package com.practice.mypractice.interview8;

public class FX82MSCalculator implements Calculator {

    @Override
    public double add(int a, int b) {
        return a + b;
    }

    @Override
    public double sub(int a, int b) {
        return a - b;
    }

    @Override
    public double multiply(int a, int b) {
        return a * b;
    }
}
